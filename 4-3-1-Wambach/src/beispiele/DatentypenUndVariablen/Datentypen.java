package beispiele.DatentypenUndVariablen;

public class Datentypen 
{
	public static void main(String[] args) 
	{
		// Wahrheitswert
		boolean a = true;
		System.out.println(a); 
		
		// char(acter) (16 Bit)
		char c = 's';
		System.out.println(c);
		
		// byte (8 Bit)
		byte b = 55;
		System.out.println(b);
		
		// short (16 Bit)
		short s = 1024;
		System.out.println(s);
		
		// Integer (32 Bit)
		int i = 100000;
		System.out.println(i);
		
		// Long (64 Bit)
		long l = 3000000000l;
		System.out.println(l);
		
		// Float (32 Bit)
		float f = 10.789f;
		System.out.println(f);
		
		// Double (64 Bit)
		double d = 10.789;
		System.out.println(d);
		
		System.out.println(10); // Integer
		System.out.println(10.0); // Double
		
		// System.out.println(0.3-0.1-0.1-0.1);
		// System.out.println(0.3-(0.1+0.1+0.1));
		
		String meinName = "\"Meine Zeichenkette\""; //Escape des Anf√ºhrungszeichens
		System.out.println(meinName);
		
		
		
	}



}
